<h2>Rules for Taylor Assassins</h2>

<p>Note: there have in the past been incidents where loopholes in the rules are exploited, so try to use some common sense. You don't want to have to read any more lawyer-babble than you have to, so don't do anything that will force new rules into existence.</p>

<h3 id="introduction">Introduction</h3>

<p>The idea is to have many people running around with nerf guns &amp; lightsabers (and other <a href="/rules.php#weapons">weapons</a>) killing each other! (In a figurative, but very real way :) ). Inside buildings and some other places are <a href="/rules.php#safe_zones">safe zones</a>. Every game's page will have the specific rules listed there for you, or you can view the <a href="/rules.php#game_types">game types</a> below for more details.</p>
<p>Note: you don't actually have to read all these rules if you don't really want to because frankly they're either common sense or things that you can learn as you go if you really want to -- however, there are some edge cases (like the <a href="/rules.php#skywalk_rule">SkyWalk rule</a>) which really could be useful to know in advance. </p>

<h3 id="rule_1">Rule #1</h3>
<p>Be awesome. Above and beyond all else, our goal is to increase the epicness of Taylor University!</p>

<h3 id="safe_zones">Safe Zones (kills do not count)</h3>
<ul>
  <li>Inside permanently-enclosed buildings (roofed and walled) or cars.</li>
  <li>When touching a door or door handle to a building or car you have authorized access to. (If the door is locked, you must provide keys upon request, or else you are considered outside.)</li>
  <li>When <i>at</i> church</li>
  <li>When <i>participating</i> in an <i>official</i> organized activity (sport practices/events, wing retreats etc.)</li>
  <li>If certain people agree on a place/circumstance being a "Safe Zone", kills do not count <i>for those who agreed to it</i>.</li>
  <li>Note: Despite being an enclosed structure, the Bergwall-DC Skywalk is <span class='bu'>NOT</span> considered a safe zone (see below).</li>
</ul>

<p>In order to attack another player, a player must <span class='bu'>NOT</span> be in a safe zone! Thus, you may not hold the door and swing your lightsaber or shoot your gun. Neither can you participate in a "drive-by" shooting.</p>

<h3 id="skywalk_rule">SkyWalk Rule</h3>
<p>The Bergwall-DC SkyWalk is considered outside because we have simply found it to be too biased for Bergwallians. As in the <a href="#safe_zones">Safe Zone</a> rule, touching the handle to a door on either side is considered "inside".</p>

<h3 id="weapons">Allowed Weapons:</h3>
<ul>
  <li>Any Nerf or Nerf-like product* (No matter how much they are modded, they still count.)</li>
  <li>Plastic lightsabers</li>
  <li>Self-made foam melee weapons</li>
  <li>Rubber band guns*</li>
  <li>Marshmallow guns*</li>
  <li>PVC Blow Guns*</li>
  <li>Breadsticks</li>
  <li>Snowballs*</li>
  <li>Note: Manually throwing a Nerf dart does <span class='bu'>NOT</span> count.</li>
  <li>Note: Bare hands by themselves are <span class='bu'>NOT</span> a weapon (unless you're a zombie).</li>
</ul>

<p><span style='font-size: small; margin-left: 3em;'>* Denotes ranged weapons</span></p>

<p>Any other weapons <span class='bu'>MUST</span> be approved IN ADVANCE by the <a href="send.php">administrator</a>.</p>

<h3 id="who">Who Can Play</h3>
<ul>
  <li>Current Taylor Students</li>
  <li>Taylor Alumni who are on Taylor's campus during the game</li>
</ul>

<h3 id="deaths">Deaths</h3>
<p>You must mark yourself as dead within 6 hours of being killed. There is no way currently to enforce that rule at all and there won't actually be any punishment, but seriously, just do it within 6 hours.</p>

<h3 id="divulging">Divulging of Information</h3>
<ul>
  <li>If you are currently <span class='b'>ALIVE</span>, you may freely divulge to anyone:
    <ul>
      <li>Any information about the observed death of another player</li>
      <li>Any information about an unsuccessful attempt of your death</li>
    </ul>
  </li>
  <li style='margin-top: 1em;'>If you are currently <span class='b'>DEAD</span>, you may divulge <span class='u'>ONLY</span>:
    <ul>
      <li>The fact that you were gruesomely killed</li>
      <li>The time of your gruesome death</li>
    </ul>
  </li>
</ul>

<h3 id="limb_rule">Limb Rule</h3>
<p>Unless explicitly stated otherwise, attacks hitting an arm or a leg will instantly put that limb out of service. The person who lost a limb must hop on one foot if a leg is lost or put the arm behind his/her back (or anywhere out of the way) if an arm is lost. It is possible to lay there armless and legless (as opposed to <a href="http://en.wikipedia.org/wiki/Legolas">Legolas</a>).</p>

<p>If you're not in a battle (for example, if your opponent cuts off your arm and then suddenly runs off), then you can stand and use your severed limbs again. But as soon as you're "in" a battle again, you can't use your severed limbs. You are "in" a battle when another player sees you and declares, implicitly or explicitly, to be in a battle with you. (But if they don't see you, then you never entered a battle.) Don't be cheesy in how you time yourself entering battles.</p>

<p>You are allowed to explicitly use a limb to block a single attack. A single attack is a single swing and/or a single projectile launched at you. For example, if someone shoots at you, you are allowed to smack the bullet with you hand and continue charging with a lightsaber.<p>

<h3 id="gauntlet_rule">Gauntlet Rule</h3>
<p>Only melee attacks <i>above</i> the wrist count, since we are assuming everyone is wearing a gauntlet made of <a href="http://starwars.wikia.com/wiki/Phrik">phrink</a>.</p>

<h3 id="blocking_rule">Blocking Rule</h3>
<p>You can block any weapon with any other weapon. We are assuming that all weapons have <a href="http://starwars.wikia.com/wiki/Phrik">phrink</a> plating.</p>

<h3 id="sheild_rule">Shield Rule</h3>
<p>No shields are allowed. A "shield" is an object which is <i>intentionally</i> used only to defend the person. Players (non-civilians) are not objects. A backpack is considered an extention of your torso (a.k.a. a hunchback) and is <span class='bu'>NOT</span> a shield.</p>

<p>Note, by the <a href="#blocking_rule">blocking rule</a>, other weapons may be used as shields. This rule is supposed to prevent people from building ridiculous defenses which make the game less enjoyable.</p>

<h3 id="article_rule">Articles Rule</h3>
<p>Anything that you are "wearing" (including backpacks and coats) is considered a part of you and can be attacked.  If it is explicitly worn on the arm/leg, then the limb rule applies. Otherwise it counts as a part of the torso or head. If it's too ambiguous if the article is worn on a limb or the torso/head, always assume it's a part of the torso/head.</p>

<p>Anything being carried is considered being "worn" and is therefore a part of that person, unless the item is discarded during a battle.  If an item is too ambiguous in regards to being "worn" or not, then assume they are wearing it.</p>

<h3 id="grab_rule">Grab Rule</h3>
<p>You are not allowed to grab another player's melee weapons in combat.</p>

<h3 id="civilian_rule">Civilian Rule</h3>
<p>You may not attack or use non-players (civilians) as shields -- <span class='b'>even if they <i>want</i> to help you</span>. What did they ever do to you?? This includes dancing around a civilian while avoiding an enemy. You may, however, hire a civilian to serve as a distraction or lookout, but only with their prior consent.</p>

<h3 id="murder_rule">Murder Rule</h3>
<p>Don't literally kill someone. Please. I claim no responsibility for your villainous actions.</p>

