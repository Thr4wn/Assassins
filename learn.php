<h2>rules</h2>

<p>
<h3>Weaponry</h3>

	<ul>
		<li>Nerf guns</li>
		<li>Rubber band guns</li>
		<li>Marshmallow guns</li>
		<li>Plastic lightsabers</li>
		<li>Breadsticks</li>
		<li><i>Any other weapon must be explicitly approved by the game director</i></li>
	</ul>
</p>

<p>
<h3>Safe Zones</h3>

	<ul>
		<li>All kills must be made outside.</li>
		<li>Safe zones include permanantly-enclosed buildings (roofed, walled) and cars.</li>
		<li>If any part of you is inside or touching the door, you are safe.</li>
		<li>Touching the door to a building or car does NOT count if you do not have access to it. (Grabbing a random parked car or locked door does not grant you immunity.)</li>
		<li>If you are in a safe zone, you may NOT kill anyone! Kills made from safe zones do not count.</li>
		<li><i>Exceptions may be made for "official" organized activities (such as wing retreats), and athletes are granted immunity when in a game...but warm-up and cool-down are open.</i><br/>
	</ul>
</p>

<p>
<h3>Deaths</h3>
	<ul>
	<li>When you are killed, log it on this site as soon as possible to allow others to start tracking down your killer and avenge your death.</li>
	<li>The <b>only</b> information you may give out about your death is:
		<ol>
			<li>The fact that you are dead</li>
			<li>The time of your death</li>
		</ol>
		<b>Anyone who discloses more than these two pieces of information may be banned from future rounds.</b>
	</li>
	<li>Dead or alive, you may give out information about someone else's death, as long as it is not connected with your own.
	For example:
	<blockquote>
		Two players walking together are hit by a sniper. They cannot give information about the ambush, even though technically they could do so without directly revealing information about their own death.
	</blockquote>
	</li>
	<li><b>Civilian casualties are expressly forbidden! Do NOT attack a non-player!</b></li>
	</ul>
</p>

<p>
<h3>Game Type: Regular</h3>
	<ul>
	<li>In the regular version of assassins, there are one or more assassins per game.</li>
	<li>The assassins win if they kill all the "good" players.</li>
	<li>The non-assassins win if they manage to kill all the assassins.</li>
	</ul>
</p>

<p>
<h3>Game Type: Zombies vs. Humans</h3>
	<ul>
	<li>In the zombies version of assassins, there are one or more people initially set to dead (i.e., they start out as zombies).</li>
	<li>All zombies must wear a brightly-coloured bandana, flag, or similar cloth on their right arm. It must be fully visible.</li>
	<li>Zombies may only use melee weapons (including hands).</li>
	<li>Humans may use any of the normally-approved weapons listed above.</li>
	<li>Zombies may not kill zombies, and humans may not kill humans.</li>
	<li>If a human dies or gets touched anywhere by a zombie, he or she turns into a zombie immediately and starts playing for the zombie side, provided they have instant access to a bandana. (Thus, it's a good idea if all players get access to a brightly-coloured bandana before the game starts.) If a human dies, they should update their status on the site as soon as possible.</li>
	<li>A zombie may only die by a direct blow to the body. If an arm or leg is hit, they may not use it until the "battle" is decided.</li>
	<li>If a zombie dies, they remain dead until the next hour begins...then they resurrect. A zombie does not get resurrected until the last chime of the bell tower sounds. Zombies must also resurrect in a safe zone...otherwise they are still dead until they make it back to a safe zone. If there are any disputes, they will be handled by the game director.</li>
	<li>If a human kills a zombie or a zombie infests a human, they should update their kill status on the game page to reflect the kill.</li>
	</ul>
</p>

